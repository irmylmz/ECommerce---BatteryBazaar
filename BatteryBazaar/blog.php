        <h3>What Is a Car Battery?</h3>
        <p>A car battery is a rechargeable power source that provides electricity to start your car’s engine and powers electrical components like lights, radio, and air conditioning. The most common type of car battery is the lead-acid battery.</p>

        <h3>How Does a Car Battery Work?</h3>
        <p>When you turn the key or press the start button, the car battery sends a burst of electricity to the starter motor, which then turns the engine over. Once the engine is running, the alternator takes over, keeping the battery charged and powering your car’s electrical systems.</p>

        <h3>Signs Your Car Battery Might Be Failing</h3>
        <ul>
          <li><strong>Slow Engine Crank:</strong> If your engine is slow to start, your battery might be losing its charge.</li>
          <li><strong>Dim Lights:</strong> Headlights and interior lights that are dimmer than usual can indicate a weak battery.</li>
          <li><strong>Warning Light:</strong> Most cars have a dashboard warning light that looks like a battery. If this light is on, there might be an issue.</li>
          <li><strong>Strange Odor:</strong> A rotten egg smell can indicate a leaking battery.</li>
          <li><strong>Corroded Connectors:</strong> Check the battery terminals (the parts where cables connect). If they’re covered in a white, ashy substance, they’re corroded.</li>
        </ul>
