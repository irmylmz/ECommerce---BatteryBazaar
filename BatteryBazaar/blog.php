        <h3>How to Take Care of Your Car Battery</h3>
        <ul>
          <li><strong>Regular Testing:</strong> Have your battery tested at least twice a year. Many auto parts stores will do this for free.</li>
          <li><strong>Clean Terminals:</strong> Keep the terminals clean and free from corrosion. You can clean them with a mixture of baking soda and water.</li>
          <li><strong>Secure the Battery:</strong> Make sure the battery is properly secured in its tray. Vibration can damage it over time.</li>
          <li><strong>Drive Regularly:</strong> Short trips don’t give your battery enough time to recharge. Try to take longer drives occasionally.</li>
          <li><strong>Avoid Electrical Drain:</strong> Turn off lights, radio, and other electrical systems when the engine is off.</li>
        </ul>

        <h3>How Long Does a Car Battery Last?</h3>
        <p>On average, a car battery lasts about 3-5 years. However, this can vary based on climate, driving habits, and how well you maintain it. In hotter climates, batteries tend to wear out faster.</p>

        <h3>What to Do If Your Battery Dies</h3>
        <p>If your battery dies, you can jump-start it using jumper cables and another vehicle with a good battery. Here’s how:</p>
        <ol>
          <li>Park the vehicles close enough for the cables to reach, but don’t let them touch.</li>
          <li>Turn off both vehicles.</li>
          <li>Attach the red (positive) cable to the positive terminal of the dead battery.</li>
          <li>Attach the other end of the red cable to the positive terminal of the good battery.</li>
          <li>Attach the black (negative) cable to the negative terminal of the good battery.</li>
          <li>Attach the other end of the black cable to an unpainted metal surface on the engine block of the dead car.</li>
          <li>Start the vehicle with the good battery and let it run for a few minutes.</li>
          <li>Try to start the dead vehicle.</li>
        </ol>
        <p>Once your car is running, drive for at least 15-30 minutes to allow the alternator to recharge the battery. If it doesn’t hold a charge, you may need to replace it.</p>
